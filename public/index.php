<script id="scroll-reveal">
  document.addEventListener('DOMContentLoaded', function() {
    const observerOptions = {
      threshold: 0.1,
      rootMargin: '0px 0px -50px 0px'
    };
    const observer = new IntersectionObserver(function(entries) {
      entries.forEach(entry => {
        if (entry.isIntersecting) {
          entry.target.classList.add('revealed');
        }
      });
    }, observerOptions);
    document.querySelectorAll('.scroll-reveal').forEach(el => {
      observer.observe(el);
    });
  });
</script>
<script id="navbar-scroll">
  document.addEventListener('DOMContentLoaded', function() {
    const navbar = document.querySelector('nav');
    let lastScrollY = window.scrollY;
    window.addEventListener('scroll', () => {
      const currentScrollY = window.scrollY;
      if (currentScrollY > 100) {
        navbar.style.transform = currentScrollY > lastScrollY ? 'translateY(-100%)' : 'translateY(0)';
      } else {
        navbar.style.transform = 'translateY(0)';
      }
      lastScrollY = currentScrollY;
    });
  });
</script>
<script id="dropdown-functionality">
  document.addEventListener('DOMContentLoaded', function() {
    const dropdownContainers = document.querySelectorAll('.dropdown-container');
    dropdownContainers.forEach(container => {
      const trigger = container.querySelector('.dropdown-trigger');
      const menu = container.querySelector('.dropdown-menu');
      let hoverTimeout;
      container.addEventListener('mouseenter', () => {
        clearTimeout(hoverTimeout);
        menu.style.opacity = '1';
        menu.style.visibility = 'visible';
        menu.style.transform = 'translateY(0)';
        trigger.querySelector('i').style.transform = 'rotate(180deg)';
      });
      container.addEventListener('mouseleave', () => {
        hoverTimeout = setTimeout(() => {
          menu.style.opacity = '0';
          menu.style.visibility = 'hidden';
          menu.style.transform = 'translateY(8px)';
          trigger.querySelector('i').style.transform = 'rotate(0deg)';
        }, 100);
      });
      trigger.addEventListener('click', (e) => {
        e.preventDefault();
        if (window.innerWidth < 768) {
          const isOpen = menu.style.opacity === '1';
          if (isOpen) {
            menu.style.opacity = '0';
            menu.style.visibility = 'hidden';
            menu.style.transform = 'translateY(8px)';
            trigger.querySelector('i').style.transform = 'rotate(0deg)';
          } else {
            menu.style.opacity = '1';
            menu.style.visibility = 'visible';
            menu.style.transform = 'translateY(0)';
            trigger.querySelector('i').style.transform = 'rotate(180deg)';
          }
        }
      });
    });
    document.addEventListener('click', (e) => {
      dropdownContainers.forEach(container => {
        if (!container.contains(e.target)) {
          const menu = container.querySelector('.dropdown-menu');
          const trigger = container.querySelector('.dropdown-trigger');
          menu.style.opacity = '0';
          menu.style.visibility = 'hidden';
          menu.style.transform = 'translateY(8px)';
          trigger.querySelector('i').style.transform = 'rotate(0deg)';
        }
      });
    });
  });
</script>

